/**
 * Get the emoji icon for a word data object.
 * Tries translationEn вҶ’ word вҶ’ falls back to category icon.
 */
function getWordIcon(word) {
    const key = (word.translationEn || '').toLowerCase().trim();
    if (key && WORD_ICONS[key]) return WORD_ICONS[key];

    // Fallback: category-level emoji
    const catFallbacks = {
        food: 'рҹҚҪпёҸ',
        color: 'рҹҺЁ',
        numbers: 'рҹ”ў',
        adjective: 'рҹ“қ',
        shopping: 'рҹӣҚпёҸ',
        health: 'вқӨпёҸ',
        orientation: 'рҹ§ӯ',
        surrounding: 'рҹҢҚ',
        communication: 'рҹ’¬',
        transport: 'рҹҡҖ',
        time: 'вҸ°',
        technology: 'рҹ’»',
        verb: 'вҡЎ',
        personal: 'рҹ‘Ө',
        sentences: 'рҹ“–',
    };
    return catFallbacks[word.category] || 'рҹ“қ';
}
